package day04asciiwrapperclassmoperatorsmemoryusage;

public class AsciiValues {

    //Java da her karakterin sayısal bir değeri vardır.
    //BU değerler ASCII deperler olarak adlandırılır.
    // Bu değerlerin tamamının bulunduğu tabloya

    public static void main(String[] args) {

        //Herhangi bir karakterin ASCII değerini bulmak için o karakteri "int" data type ında bir variable in içine koyunuz

        int ch = 'A';
        System.out.println(ch);

        int unlem = '!';
        System.out.println(unlem);

        //Java da char ları matemtiksel işlemlerde kullanırsanız, Java o char ların ASCII değerlerini kullanır.
        char c1 = 'K';
        char c2 = '?';
        System.out.println(c1+c2);//138





    }
}
