package day12loopsulehoca;

public class C02_ForLoop {

    public static void main(String[] args) {

        //1'den 100'e kadar hiç sayı kullanmadan console yazdırın.

        for (int i = 'e'/'e'; i < 'e' ; i++) {// e nin ASCII değeri 101. ==>101/101=1,
            System.out.print(i);

        }
    }
}
